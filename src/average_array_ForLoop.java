public class average_array_ForLoop {
    public static void main(String[] args) {
        float marks[]={23.4f,34.6f,67.f,56.8f,90.0f};
        float avg=0;
        for(float element:marks){
          avg=(23.4f+34.6f+67.f+56.8f+90.0f)/5;
        }
        System.out.println("the average value is :"+avg);
    }
}
